# string slicing
name = "Ananya"
#  index concept = we start counting with 0 and in backward we start with -1
nameshorts = len(name) #function to find length of string
print(nameshorts)

# slice strings 
nameshort = name[0:3] #this means start from string 0 all the way till 3 (3 not included )
print(nameshort)

character1 = name[1]
print(character1) #character is used to one char. 


